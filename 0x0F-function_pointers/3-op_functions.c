#include "3-calc.h"

/**
 * op_add - adds two ints
 *
 * @a: number
 * @b: number
 * Return: returns sum of a, b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts two ints
 *
 * @a: number
 * @b: number
 * Return: returns difference of a, b
 */


int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplies two ints
 *
 * @a: number
 * @b: number
 * Return: returns multiplication of a, b
 */


int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two ints
 *
 * @a: number
 * @b: number
 * Return: returns division of a, b
 */


int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds remainder of two ints when divided
 *
 * @a: number
 * @b: number
 * Return: returns remander of a, b when divided
 */


int op_mod(int a, int b)
{
	return (a % b);
}
